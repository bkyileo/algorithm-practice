#include<bits/stdc++.h>
using namespace std;
vector<vector<int>> arr(21,vector<int>(21));
int num;
bool ok[21];
void dfs(int curr,int &res)
{
	if(curr==num)
	{
		res++;
		return ;
	}
	for(int i=0;i<num;++i)
	{
		if(ok[i]&&arr[curr][i])
		{
			ok[i]=false;
			dfs(curr+1,res);
			ok[i]=true;
		}
	}
	return ;
}
int main()
{
	int n;cin>>n;
	while(n--)
	{
		cin>>num;
		for(int i=0;i<num;++i)
		for(int j=0;j<num;++j)
		cin>>arr[i][j];
		memset(ok,true,sizeof(ok));	
		int res=0;
		dfs(0,res);
		cout<<res<<endl;
	}
	return 0;
} 
